                function envia() {//envia formulario via ajax para email
                    var nome = $('#name').val();
                    var email = $('#email').val();
                    var assunto = $('#subject').val();
                    var mensagem = $('#message').val();

                    $.ajax({
                        type: "POST",
                        crossDomain: true,
                        url: "email.php",
                        data: {
                            'nome': nome,
                            'email': email,
                            'assunto': assunto,
                            'message': mensagem
                        },
                        success: function (data) {
                            alert("Mensagem enviada com sucesso");
                            location.href = 'contato.php'
                        },
                        error: function (data) {
                            alert("erro ao enviar");
                            location.href = 'contato.php'
                        }
